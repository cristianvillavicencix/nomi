create or replace function public.generate_payment_lines(p_payment_id bigint)
returns integer
language plpgsql
security invoker
as $$
declare
  v_payment public.payments%rowtype;
  v_inserted integer := 0;
  v_rowcount integer := 0;
begin
  select * into v_payment from public.payments where id = p_payment_id;
  if v_payment is null then
    raise exception 'Payment % not found', p_payment_id;
  end if;

  insert into public.payment_lines (
    payment_id,
    person_id,
    project_id,
    source_type,
    source_id,
    qty_hours,
    rate,
    amount,
    notes
  )
  select
    v_payment.id,
    te.person_id,
    te.project_id,
    'time_entry',
    te.id,
    te.hours,
    case
      when p.pay_type = 'hourly' then coalesce(p.hourly_rate, 0)
      when p.pay_type = 'day_rate' then coalesce(p.day_rate, 0) / 8
      else 0
    end as rate,
    round(
      te.hours * case
        when p.pay_type = 'hourly' then coalesce(p.hourly_rate, 0)
        when p.pay_type = 'day_rate' then coalesce(p.day_rate, 0) / 8
        else 0
      end,
      2
    ) as amount,
    concat('Generated from time entry #', te.id)
  from public.time_entries te
  inner join public.people p on p.id = te.person_id
  where te.org_id = v_payment.org_id
    and p.org_id = v_payment.org_id
    and te.status = 'approved'
    and te.date between v_payment.pay_period_start and v_payment.pay_period_end
    and not exists (
      select 1
      from public.payment_lines pl
      where pl.source_type = 'time_entry'
        and pl.source_id = te.id
    );

  get diagnostics v_rowcount = row_count;
  v_inserted := v_inserted + v_rowcount;

  insert into public.payment_lines (
    payment_id,
    person_id,
    project_id,
    source_type,
    source_id,
    qty_hours,
    rate,
    amount,
    notes
  )
  select
    v_payment.id,
    p.id,
    d.id,
    'commission',
    d.id,
    null,
    p.commission_rate,
    round((coalesce(d.amount, 0)::numeric * coalesce(p.commission_rate, 0)) / 100, 2),
    concat('Generated commission from project #', d.id)
  from public.deals d
  inner join public.people p on p.id = d.sales_id
  where p.type = 'salesperson'
    and p.org_id = v_payment.org_id
    and d.stage = 'won'
    and coalesce(d.expected_closing_date::date, d.created_at::date) between v_payment.pay_period_start and v_payment.pay_period_end
    and not exists (
      select 1
      from public.payment_lines pl
      where pl.source_type = 'commission'
        and pl.source_id = d.id
        and pl.person_id = p.id
    );

  get diagnostics v_rowcount = row_count;
  v_inserted := v_inserted + v_rowcount;

  update public.payments py
  set total_gross = lines.total_gross,
      total_net = lines.total_gross
  from (
    select payment_id, round(coalesce(sum(amount), 0), 2) as total_gross
    from public.payment_lines
    where payment_id = v_payment.id
    group by payment_id
  ) lines
  where py.id = lines.payment_id;

  return v_inserted;
end;
$$;

create or replace function public.mark_payment_time_entries_paid()
returns trigger
language plpgsql
as $$
begin
  if new.status = 'paid' and old.status is distinct from 'paid' then
    update public.time_entries te
    set status = 'paid'
    from public.payment_lines pl
    where pl.payment_id = new.id
      and pl.source_type = 'time_entry'
      and pl.source_id = te.id
      and te.status = 'approved';
  end if;
  return new;
end;
$$;

drop trigger if exists payments_mark_time_entries_paid on public.payments;
create trigger payments_mark_time_entries_paid
after update of status on public.payments
for each row
execute function public.mark_payment_time_entries_paid();

create or replace view public.report_project_profitability
with (security_invoker=on)
as
select
  d.id as id,
  d.id as project_id,
  d.name as project_name,
  coalesce(sum(te.hours), 0) as total_hours,
  round(
    coalesce(sum(
      case
        when p.pay_type = 'hourly' then te.hours * coalesce(p.hourly_rate, 0)
        when p.pay_type = 'day_rate' then te.hours * (coalesce(p.day_rate, 0) / 8)
        else 0
      end
    ), 0),
    2
  ) as total_labor_cost,
  coalesce(d.amount, 0)::numeric as total_revenue,
  round(
    coalesce(d.amount, 0)::numeric - coalesce(sum(
      case
        when p.pay_type = 'hourly' then te.hours * coalesce(p.hourly_rate, 0)
        when p.pay_type = 'day_rate' then te.hours * (coalesce(p.day_rate, 0) / 8)
        else 0
      end
    ), 0),
    2
  ) as profit,
  coalesce(max(te.date), d.created_at::date) as metric_date
from public.deals d
left join public.time_entries te on te.project_id = d.id and te.status in ('approved', 'paid')
left join public.people p on p.id = te.person_id
group by d.id, d.name, d.amount, d.created_at;

create or replace view public.report_payroll_summary
with (security_invoker=on)
as
select
  py.id,
  py.id as payment_id,
  py.org_id,
  py.pay_period_start,
  py.pay_period_end,
  py.pay_date,
  py.status,
  round(coalesce(sum(pl.amount), 0), 2) as total_gross,
  round(coalesce(sum(pl.amount), 0), 2) as total_net,
  count(pl.id) as lines_count
from public.payments py
left join public.payment_lines pl on pl.payment_id = py.id
group by py.id, py.org_id, py.pay_period_start, py.pay_period_end, py.pay_date, py.status;

create or replace view public.report_labor_cost_by_person
with (security_invoker=on)
as
select
  concat(te.person_id::text, '-', to_char(date_trunc('month', te.date), 'YYYY-MM')) as id,
  te.person_id,
  p.org_id,
  concat(p.first_name, ' ', p.last_name) as person_name,
  date_trunc('month', te.date)::date as period_month,
  count(te.id) as entries_count,
  coalesce(sum(te.hours), 0) as total_hours,
  round(
    coalesce(sum(
      case
        when p.pay_type = 'hourly' then te.hours * coalesce(p.hourly_rate, 0)
        when p.pay_type = 'day_rate' then te.hours * (coalesce(p.day_rate, 0) / 8)
        when p.pay_type = 'salary' then 0
        when p.pay_type = 'commission' then 0
        else 0
      end
    ), 0),
    2
  ) as total_labor_cost
from public.time_entries te
inner join public.people p on p.id = te.person_id
where te.status in ('approved', 'paid')
group by
  te.person_id,
  p.org_id,
  p.first_name,
  p.last_name,
  date_trunc('month', te.date);
  
create or replace view public.report_sales_commissions_by_salesperson
with (security_invoker=on)
as
select
  concat(pl.person_id::text, '-', py.id::text) as id,
  pl.person_id,
  py.id as payment_id,
  py.org_id,
  py.pay_date,
  concat(p.first_name, ' ', p.last_name) as salesperson_name,
  count(pl.id) as commission_lines,
  round(coalesce(sum(pl.amount), 0), 2) as total_commission
from public.payment_lines pl
inner join public.payments py on py.id = pl.payment_id
inner join public.people p on p.id = pl.person_id
where pl.source_type = 'commission'
  and p.type = 'salesperson'
group by pl.person_id, py.id, py.org_id, py.pay_date, p.first_name, p.last_name;

