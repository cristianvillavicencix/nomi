create table if not exists public.people (
  id bigint generated by default as identity primary key,
  org_id bigint not null default 1,
  type text not null check (type in ('employee', 'salesperson', 'subcontractor')),
  first_name text not null,
  last_name text not null,
  phone text,
  email text,
  status text not null default 'active' check (status in ('active', 'inactive')),
  pay_type text not null check (pay_type in ('hourly', 'salary', 'commission', 'day_rate')),
  hourly_rate numeric(12,2),
  salary_rate numeric(12,2),
  day_rate numeric(12,2),
  commission_rate numeric(8,4),
  created_at timestamp with time zone not null default now()
);

create table if not exists public.time_entries (
  id bigint generated by default as identity primary key,
  org_id bigint not null default 1,
  person_id bigint not null references public.people(id) on update cascade on delete restrict,
  project_id bigint not null references public.deals(id) on update cascade on delete restrict,
  date date not null,
  hours numeric(8,2) not null check (hours >= 0),
  start_time time,
  end_time time,
  notes text,
  status text not null default 'draft' check (status in ('draft', 'approved', 'paid')),
  created_at timestamp with time zone not null default now()
);

create table if not exists public.payments (
  id bigint generated by default as identity primary key,
  org_id bigint not null default 1,
  pay_period_start date not null,
  pay_period_end date not null,
  pay_date date not null,
  status text not null default 'draft' check (status in ('draft', 'approved', 'paid')),
  total_gross numeric(14,2),
  total_net numeric(14,2),
  created_at timestamp with time zone not null default now(),
  check (pay_period_start <= pay_period_end)
);

create table if not exists public.payment_lines (
  id bigint generated by default as identity primary key,
  payment_id bigint not null references public.payments(id) on update cascade on delete cascade,
  person_id bigint not null references public.people(id) on update cascade on delete restrict,
  project_id bigint references public.deals(id) on update cascade on delete set null,
  source_type text not null check (source_type in ('time_entry', 'salary', 'commission', 'adjustment')),
  source_id bigint,
  qty_hours numeric(8,2),
  rate numeric(12,2),
  amount numeric(14,2) not null,
  notes text,
  created_at timestamp with time zone not null default now()
);

