-- Website uptime monitoring for client/lead company URLs.

create table if not exists public.monitored_websites (
  id bigint generated by default as identity primary key,
  org_id bigint not null references public.organizations (id) on delete cascade,
  company_id bigint references public.companies (id) on update cascade on delete set null,
  contact_id bigint references public.contacts (id) on update cascade on delete set null,
  url text not null,
  display_name text,
  source text not null default 'company'
    check (source in ('company', 'contact', 'manual')),
  is_enabled boolean not null default true,
  slow_threshold_ms integer not null default 3000
    check (slow_threshold_ms > 0),
  check_interval_minutes integer not null default 5
    check (check_interval_minutes >= 1),
  last_status text not null default 'unknown'
    check (last_status in ('up', 'slow', 'down', 'unknown')),
  last_response_ms integer,
  last_http_status integer,
  last_checked_at timestamptz,
  last_error text,
  ssl_expires_at timestamptz,
  ssl_days_remaining integer,
  dns_ip text,
  dns_nameservers text[] not null default '{}',
  dns_mx text[] not null default '{}',
  hosting_provider text,
  hosting_confidence text
    check (hosting_confidence is null or hosting_confidence in ('low', 'medium', 'high')),
  tech_stack text[] not null default '{}',
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (org_id, url)
);

create index if not exists monitored_websites_org_id_idx
  on public.monitored_websites (org_id);

create index if not exists monitored_websites_company_id_idx
  on public.monitored_websites (company_id)
  where company_id is not null;

create index if not exists monitored_websites_last_status_idx
  on public.monitored_websites (org_id, last_status);

create index if not exists monitored_websites_enabled_due_idx
  on public.monitored_websites (is_enabled, last_checked_at)
  where is_enabled = true;

create table if not exists public.website_checks (
  id bigint generated by default as identity primary key,
  org_id bigint not null references public.organizations (id) on delete cascade,
  monitored_website_id bigint not null references public.monitored_websites (id) on delete cascade,
  checked_at timestamptz not null default now(),
  status text not null check (status in ('up', 'slow', 'down')),
  response_ms integer,
  http_status integer,
  error_message text,
  ssl_days_remaining integer,
  metadata jsonb not null default '{}'::jsonb
);

create index if not exists website_checks_site_checked_idx
  on public.website_checks (monitored_website_id, checked_at desc);

create index if not exists website_checks_org_checked_idx
  on public.website_checks (org_id, checked_at desc);

alter table public.monitored_websites enable row level security;
alter table public.website_checks enable row level security;

grant delete, insert, select, update on table public.monitored_websites to authenticated;
grant delete, insert, references, select, trigger, truncate, update on table public.monitored_websites to service_role;

grant delete, insert, select, update on table public.website_checks to authenticated;
grant delete, insert, references, select, trigger, truncate, update on table public.website_checks to service_role;

