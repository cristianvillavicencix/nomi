create or replace function public.normalize_website_monitor_url(raw_url text)
returns text
language plpgsql
immutable
as $$
declare
  trimmed text;
  candidate text;
begin
  trimmed := nullif(trim(both from coalesce(raw_url, '')), '');
  if trimmed is null then
    return null;
  end if;

  candidate := trimmed;
  if candidate !~* '^https?://' then
    candidate := 'https://' || candidate;
  end if;

  if candidate !~* '^https?://[a-z0-9]([a-z0-9-]*[a-z0-9])?(\.[a-z0-9]([a-z0-9-]*[a-z0-9])?)+(/.*)?$' then
    return null;
  end if;

  return trim(trailing '/' from candidate);
end;
$$;

create or replace function public.sync_monitored_websites_for_org(target_org_id bigint)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  upserted_count integer := 0;
begin
  insert into public.monitored_websites (
    org_id,
    company_id,
    url,
    display_name,
    source,
    is_enabled
  )
  select distinct on (c.org_id, public.normalize_website_monitor_url(c.website))
    c.org_id,
    c.id,
    public.normalize_website_monitor_url(c.website),
    c.name,
    'company',
    true
  from public.companies c
  where c.org_id = target_org_id
    and public.normalize_website_monitor_url(c.website) is not null
  order by c.org_id, public.normalize_website_monitor_url(c.website), c.id
  on conflict (org_id, url) do update
  set
    company_id = excluded.company_id,
    display_name = coalesce(excluded.display_name, public.monitored_websites.display_name),
    source = 'company',
    is_enabled = true,
    updated_at = now();

  get diagnostics upserted_count = row_count;

  update public.monitored_websites mw
  set
    is_enabled = false,
    updated_at = now()
  where mw.org_id = target_org_id
    and mw.source = 'company'
    and mw.company_id is not null
    and not exists (
      select 1
      from public.companies c
      where c.id = mw.company_id
        and public.normalize_website_monitor_url(c.website) = mw.url
        and c.org_id = target_org_id
    );

  return upserted_count;
end;
$$;

grant execute on function public.sync_monitored_websites_for_org(bigint) to authenticated;
grant execute on function public.sync_monitored_websites_for_org(bigint) to service_role;

create or replace function public.trg_company_sync_monitored_website()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  normalized_url text;
  previous_url text;
  url_changed boolean := false;
begin
  normalized_url := public.normalize_website_monitor_url(new.website);

  if tg_op = 'UPDATE' then
    previous_url := public.normalize_website_monitor_url(old.website);
    url_changed := previous_url is distinct from normalized_url;
  end if;

  if normalized_url is null then
    update public.monitored_websites
    set is_enabled = false, updated_at = now()
    where company_id = new.id and org_id = new.org_id;
    return new;
  end if;

  insert into public.monitored_websites (
    org_id,
    company_id,
    url,
    display_name,
    source,
    is_enabled
  )
  values (
    new.org_id,
    new.id,
    normalized_url,
    new.name,
    'company',
    true
  )
  on conflict (org_id, url) do update
  set
    company_id = excluded.company_id,
    display_name = excluded.display_name,
    is_enabled = true,
    updated_at = now();

  update public.monitored_websites
  set
    is_enabled = false,
    updated_at = now()
  where org_id = new.org_id
    and company_id = new.id
    and source = 'company'
    and url is distinct from normalized_url;

  if tg_op = 'INSERT' or url_changed then
    update public.monitored_websites
    set
      last_status = 'unknown',
      last_response_ms = null,
      last_http_status = null,
      last_checked_at = null,
      last_error = null,
      ssl_expires_at = null,
      ssl_days_remaining = null,
      dns_ip = null,
      dns_nameservers = '{}',
      dns_mx = '{}',
      hosting_provider = null,
      hosting_confidence = null,
      tech_stack = '{}',
      metadata = '{}'::jsonb,
      updated_at = now()
    where org_id = new.org_id
      and url = normalized_url
      and company_id = new.id;
  end if;

  return new;
end;
$$;

drop trigger if exists trg_companies_sync_monitored_website on public.companies;
create trigger trg_companies_sync_monitored_website
  after insert or update of website, name on public.companies
  for each row
  when (new.website is not null and trim(new.website) <> '')
  execute function public.trg_company_sync_monitored_website();

create or replace view public.monitored_websites_summary
with (security_invoker = true)
as
select
  mw.*,
  c.name as company_name,
  (
    select round(
      100.0 * count(*) filter (where wc.status in ('up', 'slow'))
      / nullif(count(*), 0),
      1
    )
    from public.website_checks wc
    where wc.monitored_website_id = mw.id
      and wc.checked_at >= now() - interval '7 days'
  ) as uptime_pct_7d,
  (
    select round(avg(wc.response_ms)::numeric, 0)
    from public.website_checks wc
    where wc.monitored_website_id = mw.id
      and wc.checked_at >= now() - interval '7 days'
      and wc.response_ms is not null
  ) as avg_response_ms_7d
from public.monitored_websites mw
left join public.companies c on c.id = mw.company_id;

grant select on public.monitored_websites_summary to authenticated;
grant select on public.monitored_websites_summary to service_role;

comment on table public.monitored_websites is
  'Client/lead websites monitored for uptime and marketing outreach signals.';
comment on table public.website_checks is
  'Historical uptime checks for monitored websites.';

do $$
declare
  org_row record;
begin
  for org_row in select id from public.organizations loop
    perform public.sync_monitored_websites_for_org(org_row.id);
  end loop;
end;
$$;
