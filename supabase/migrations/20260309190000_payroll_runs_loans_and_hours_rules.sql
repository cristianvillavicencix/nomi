create table if not exists public.payroll_runs (
  id bigint generated by default as identity primary key,
  org_id bigint not null default 1,
  category text not null check (category in ('hourly', 'salaried', 'subcontractor', 'sales_commissions', 'mixed')),
  pay_schedule text not null check (pay_schedule in ('weekly', 'biweekly', 'semimonthly', 'monthly')),
  pay_period_start date not null,
  pay_period_end date not null,
  payday date not null,
  status text not null default 'draft' check (status in ('draft', 'reviewed', 'approved', 'paid', 'cancelled')),
  created_by text,
  created_at timestamp with time zone not null default now(),
  approved_at timestamp with time zone,
  paid_at timestamp with time zone,
  notes text,
  check (pay_period_start <= pay_period_end)
);

alter table public.time_entries
  drop constraint if exists time_entries_payroll_run_id_fkey;

alter table public.time_entries
  add constraint time_entries_payroll_run_id_fkey
  foreign key (payroll_run_id) references public.payroll_runs(id)
