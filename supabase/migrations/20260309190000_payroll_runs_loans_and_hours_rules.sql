create or replace function public.generate_payroll_run(p_payroll_run_id bigint)
returns integer
language plpgsql
security invoker
as $$
declare
  v_run public.payroll_runs%rowtype;
  v_inserted integer := 0;
  v_rowcount integer := 0;
  v_expected_days integer := 0;
begin
  select * into v_run from public.payroll_runs where id = p_payroll_run_id;
  if v_run is null then
    raise exception 'Payroll run % not found', p_payroll_run_id;
  end if;

  v_expected_days := greatest(1, (v_run.pay_period_end - v_run.pay_period_start + 1));

  insert into public.payroll_run_lines (
    payroll_run_id,
    employee_id,
    compensation_type,
    payment_method,
    regular_hours,
    overtime_hours,
    paid_leave_hours,
    base_salary_amount,
    unpaid_absence_deduction,
    gross_pay,
    total_deductions,
    net_pay,
    payment_notes
  )
  select
    v_run.id,
    p.id,
    p.compensation_type,
    coalesce(p.payment_method, 'bank_deposit'),
    case when p.compensation_type = 'hourly' then coalesce(sum(te.regular_hours), 0) else null end,
    case when p.compensation_type = 'hourly' then coalesce(sum(te.overtime_hours), 0) else null end,
    case when p.compensation_type = 'hourly' then coalesce(sum(
      case when te.day_type in ('holiday', 'sick_day', 'vacation_day', 'day_off') then coalesce(te.payable_hours, 0) else 0 end
    ), 0) else null end,
    case
      when p.compensation_type = 'weekly_salary' then coalesce(p.weekly_salary_amount, 0)
      when p.compensation_type = 'biweekly_salary' then coalesce(p.biweekly_salary_amount, 0)
      when p.compensation_type = 'monthly_salary' then coalesce(p.monthly_salary_amount, 0)
      else null
    end,
    0,
    round(
      case
        when p.compensation_type = 'hourly' then
          coalesce(sum(te.regular_hours), 0) * coalesce(p.hourly_rate, 0)
          + coalesce(sum(te.overtime_hours), 0) * coalesce(p.hourly_rate, 0) * coalesce(p.overtime_rate_multiplier, 1.5)
          + coalesce(sum(case when te.day_type in ('holiday', 'sick_day', 'vacation_day', 'day_off') then coalesce(te.payable_hours, 0) else 0 end), 0) * coalesce(p.hourly_rate, 0)
        when p.compensation_type = 'weekly_salary' then coalesce(p.weekly_salary_amount, 0)
        when p.compensation_type = 'biweekly_salary' then coalesce(p.biweekly_salary_amount, 0)
        when p.compensation_type = 'monthly_salary' then coalesce(p.monthly_salary_amount, 0)
        else 0
      end,
      2
    ),
    0,
    round(
      case
        when p.compensation_type = 'hourly' then
          coalesce(sum(te.regular_hours), 0) * coalesce(p.hourly_rate, 0)
          + coalesce(sum(te.overtime_hours), 0) * coalesce(p.hourly_rate, 0) * coalesce(p.overtime_rate_multiplier, 1.5)
          + coalesce(sum(case when te.day_type in ('holiday', 'sick_day', 'vacation_day', 'day_off') then coalesce(te.payable_hours, 0) else 0 end), 0) * coalesce(p.hourly_rate, 0)
        when p.compensation_type = 'weekly_salary' then coalesce(p.weekly_salary_amount, 0)
        when p.compensation_type = 'biweekly_salary' then coalesce(p.biweekly_salary_amount, 0)
        when p.compensation_type = 'monthly_salary' then coalesce(p.monthly_salary_amount, 0)
        else 0
      end,
      2
    ),
    'Generated by payroll run engine'
  from public.people p
  left join public.time_entries te on te.person_id = p.id
    and te.status = 'approved'
    and te.date between v_run.pay_period_start and v_run.pay_period_end
  where p.org_id = v_run.org_id
    and p.employee_status = 'active'
    and p.compensation_type in ('hourly', 'weekly_salary', 'biweekly_salary', 'monthly_salary')
    and not exists (
      select 1
      from public.payroll_run_lines prl
      where prl.payroll_run_id = v_run.id
        and prl.employee_id = p.id
    )
  group by p.id;

  get diagnostics v_rowcount = row_count;
  v_inserted := v_inserted + v_rowcount;

  update public.time_entries te
  set status = 'included_in_payroll',
      included_in_payroll = true,
      payroll_run_id = v_run.id
  where te.status = 'approved'
    and te.date between v_run.pay_period_start and v_run.pay_period_end
    and te.person_id in (
      select prl.employee_id from public.payroll_run_lines prl where prl.payroll_run_id = v_run.id
    );

  return v_inserted;
end;
$$;

create or replace function public.mark_payroll_run_entries_paid()
returns trigger
language plpgsql
as $$
begin
  if new.status = 'paid' and old.status is distinct from 'paid' then
    update public.time_entries te
    set status = 'paid',
        included_in_payroll = true,
        payroll_run_id = new.id
    where te.payroll_run_id = new.id;
  end if;

  return new;
end;
$$;

drop trigger if exists payroll_runs_mark_entries_paid on public.payroll_runs;
create trigger payroll_runs_mark_entries_paid
after update of status on public.payroll_runs
for each row
execute function public.mark_payroll_run_entries_paid();
