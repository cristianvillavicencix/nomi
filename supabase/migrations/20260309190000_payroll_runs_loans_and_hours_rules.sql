  employee_id bigint not null references public.people(id) on update cascade on delete restrict,
  compensation_type text not null check (compensation_type in ('hourly', 'weekly_salary', 'biweekly_salary', 'monthly_salary')),
  payment_method text not null check (payment_method in ('cash', 'check', 'zelle', 'bank_deposit')),
  regular_hours numeric(8,2),
  overtime_hours numeric(8,2),
  paid_leave_hours numeric(8,2),
  base_salary_amount numeric(14,2),
  unpaid_absence_deduction numeric(14,2) default 0,
  loan_deductions numeric(14,2) default 0,
  other_deductions numeric(14,2) default 0,
  gross_pay numeric(14,2) not null default 0,
  total_deductions numeric(14,2) not null default 0,
  net_pay numeric(14,2) not null default 0,
  payment_reference text,
  payment_notes text,
  created_at timestamp with time zone not null default now()
