-- ---------------------------------------------------------------------------
-- updated_at trigger helper (reuse if already present)
-- ---------------------------------------------------------------------------
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists trg_website_audits_updated_at on public.website_audits;
create trigger trg_website_audits_updated_at
  before update on public.website_audits
  for each row execute function public.set_updated_at();

-- ---------------------------------------------------------------------------
-- Realtime (live audit status in UI)
-- ---------------------------------------------------------------------------
alter table public.website_audits replica identity full;

do $$
begin
  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'website_audits'
  ) then
    alter publication supabase_realtime add table public.website_audits;
  end if;
end $$;

-- ---------------------------------------------------------------------------
-- Summary view: latest audit per site
-- ---------------------------------------------------------------------------
create or replace view public.website_audits_latest
with (security_invoker = true)
as
select distinct on (wa.monitored_website_id)
  wa.*
from public.website_audits wa
order by wa.monitored_website_id, wa.requested_at desc;

grant select on public.website_audits_latest to authenticated;
grant select on public.website_audits_latest to service_role;
